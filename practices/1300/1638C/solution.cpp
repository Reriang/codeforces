#include <bits/stdc++.h>
using namespace std;

// 1638C. Inversion Graph, https://codeforces.com/problemset/problem/1638/C

void solve() {
    size_t n; cin >> n;
    vector<size_t> permutation(n);
    for (auto &e : permutation) cin >> e;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1; cin >> t;
    while (t--) solve();

    return 0;
}
